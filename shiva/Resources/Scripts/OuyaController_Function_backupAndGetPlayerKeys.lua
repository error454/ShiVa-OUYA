function OuyaController.backupAndGetPlayerKeys ( nJoypad )
--------------------------------------------------------------------------------
	
	local hash, hashOld = this.getPlayerHash ( nJoypad )
    
    hashtable.copy ( hashOld, hash )
	
    return hash
	
--------------------------------------------------------------------------------
end
